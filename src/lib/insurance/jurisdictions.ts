// Helper functions
export function getJurisdictionByCode(code: string): JurisdictionConfig | undefined {
  return ALL_JURISDICTIONS.find(j => j.code === code);
}

export function getJurisdictionsByRegion(region: string): JurisdictionConfig[] {
  return ALL_JURISDICTIONS.filter(j => j.region === region);
}

export function getFilingRequirement(jurisdictionCode: string, filingId: string): FilingRequirement | undefined {
  const jurisdiction = getJurisdictionByCode(jurisdictionCode);
  return jurisdiction?.filingRequirements.find(f => f.id === filingId);
}

export function getUpcomingFilings(jurisdictionCode: string, days: number = 90): FilingRequirement[] {
  const jurisdiction = getJurisdictionByCode(jurisdictionCode);
  if (!jurisdiction) return [];

  const now = new Date();
  const futureDate = new Date(now.getTime() + days * 24 * 60 * 60 * 1000);

  return jurisdiction.filingRequirements.filter(filing => {
    const dueDate = getNextDueDate(filing);
    return dueDate >= now && dueDate <= futureDate;
  });
}

export function getNextDueDate(filing: FilingRequirement): Date {
  const now = new Date();
  const year = now.getFullYear();
  
  let dueDate: Date;
  
  if (filing.type === 'ANNUAL' && filing.dueMonth) {
    dueDate = new Date(year, filing.dueMonth - 1, filing.dueDay);
    if (dueDate < now) {
      dueDate = new Date(year + 1, filing.dueMonth - 1, filing.dueDay);
    }
  } else if (filing.type === 'QUARTERLY') {
    const currentQuarter = Math.floor(now.getMonth() / 3);
    const quarterEndMonth = (currentQuarter + 1) * 3;
    dueDate = new Date(year, quarterEndMonth, filing.dueDay);
    if (dueDate < now) {
      const nextQuarterEndMonth = ((currentQuarter + 1) % 4 + 1) * 3;
      dueDate = new Date(nextQuarterEndMonth < quarterEndMonth ? year + 1 : year, nextQuarterEndMonth, filing.dueDay);
    }
  } else {
    dueDate = new Date(year, now.getMonth() + 1, filing.dueDay);
    if (dueDate < now) {
      dueDate = new Date(year, now.getMonth() + 2, filing.dueDay);
    }
  }
  
  return dueDate;
}

export function getDaysUntilDue(filing: FilingRequirement): number {
  const dueDate = getNextDueDate(filing);
  const now = new Date();
  return Math.ceil((dueDate.getTime() - now.getTime()) / (1000 * 60 * 60 * 24));
}

export function isFilingOverdue(filing: FilingRequirement, submittedDate?: Date): boolean {
  if (submittedDate) return false;
  return getDaysUntilDue(filing) < 0;
}

export function getComplianceRiskLevel(jurisdictionCode: string, metrics: {
  solvencyMargin: number;
  capitalRatio: number;
  equityPercent: number;
  singleIssuerPercent: number;
}): 'LOW' | 'MEDIUM' | 'HIGH' | 'CRITICAL' {
  const jurisdiction = getJurisdictionByCode(jurisdictionCode);
  if (!jurisdiction) return 'HIGH';

  let riskScore = 0;

  // Check solvency margin
  if (metrics.solvencyMargin < jurisdiction.capitalRequirements.solvencyMarginPercent * 0.8) {
    riskScore += 3;
  } else if (metrics.solvencyMargin < jurisdiction.capitalRequirements.solvencyMarginPercent) {
    riskScore += 1;
  }

  // Check capital ratio
  if (metrics.capitalRatio < jurisdiction.capitalRequirements.capitalAdequacyRatio * 0.8) {
    riskScore += 3;
  } else if (metrics.capitalRatio < jurisdiction.capitalRequirements.capitalAdequacyRatio) {
    riskScore += 1;
  }

  // Check investment limits
  if (metrics.equityPercent > jurisdiction.investmentLimits.maxEquityPercent) {
    riskScore += 2;
  }
  if (metrics.singleIssuerPercent > jurisdiction.investmentLimits.maxSingleIssuerPercent) {
    riskScore += 2;
  }

  if (riskScore >= 6) return 'CRITICAL';
  if (riskScore >= 4) return 'HIGH';
  if (riskScore >= 2) return 'MEDIUM';
  return 'LOW';
}

