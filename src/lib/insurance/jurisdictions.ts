/**
 * Enterprise Regulatory Filing System - Jurisdiction Configuration
 * Supports 50+ jurisdictions across Caribbean, Latin America, Europe, North America, Asia-Pacific, and Africa
 */

export interface FilingRequirement {
  id: string;
  type: 'QUARTERLY' | 'ANNUAL' | 'SEMI_ANNUAL' | 'MONTHLY' | 'ADHOC';
  name: string;
  description: string;
  dueDay: number; // Day of month or quarter end
  dueMonth?: number; // For annual filings
  graceDays: number;
  dataFormat: 'XML' | 'JSON' | 'CSV' | 'PDF' | 'XBRL';
  templateId?: string;
  requiredFields: string[];
  validationRules: ValidationRule[];
  penaltyAmount?: number;
  penaltyCurrency?: string;
}

export interface ValidationRule {
  id: string;
  field: string;
  rule: 'REQUIRED' | 'MIN_VALUE' | 'MAX_VALUE' | 'FORMAT' | 'RANGE' | 'CUSTOM';
  value?: string | number;
  message: string;
}

export interface JurisdictionConfig {
  code: string;
  name: string;
  region: string;
  regulator: {
    name: string;
    shortName: string;
    website: string;
    email: string;
    phone: string;
    address: string;
  };
  currency: string;
  timezone: string;
  filingRequirements: FilingRequirement[];
  capitalRequirements: {
    minimumCapital: number;
    solvencyMarginPercent: number;
    capitalAdequacyRatio: number;
  };
  investmentLimits: {
    maxEquityPercent: number;
    maxSingleIssuerPercent: number;
    maxPropertyPercent: number;
    maxForeignInvestmentPercent: number;
    restrictedAssets: string[];
  };
  governanceRequirements: {
    minBoardMembers: number;
    independentDirectorsPercent: number;
    auditCommitteeRequired: boolean;
    riskCommitteeRequired: boolean;
    actuaryRequired: boolean;
    complianceOfficerRequired: boolean;
  };
  dataFormatPreferences: ('XML' | 'JSON' | 'CSV' | 'PDF' | 'XBRL')[];
  electronicSubmissionEnabled: boolean;
  digitalSignatureRequired: boolean;
  reportingBasis: 'STATUTORY' | 'GAAP' | 'IFRS' | 'LOCAL';
}

